#!/usr/bin/env python3
"""
hello_tools.py - Tool definitions and handlers for the coding agent.

Provides:
  - WORKDIR        : workspace root path
  - safe_path      : sandbox path resolution (raises ValueError on escape)
  - run_bash       : execute a shell command inside WORKDIR
  - run_read       : read file contents with optional line limit
  - run_write      : write content to a file (creates parents if needed)
  - run_edit       : replace first occurrence of old_text with new_text in a file
  - TOOL_HANDLERS  : dispatch map  {tool_name: callable(**kw) -> str}
  - TOOLS          : Anthropic tool schema definitions list
"""

import os
import subprocess
from pathlib import Path

# ęĄęĄ Workspace root ęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄ
WORKDIR = Path(r'D:\develop\CPP\learn-claude-code')


# ęĄęĄ Safe path helper ęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄ
def safe_path(p: str) -> Path:
    """Resolve *p* relative to WORKDIR and raise if it escapes the sandbox."""
    path = (WORKDIR / p).resolve()
    if not path.is_relative_to(WORKDIR):
        raise ValueError(f"Path escapes workspace: {p}")
    return path


# ęĄęĄ Tool implementations ęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄ
def run_bash(command: str) -> str:
    """Run a shell command inside WORKDIR with a 120-second timeout."""
    dangerous = ["rm -rf /", "sudo", "shutdown", "reboot", "> /dev/"]
    if any(d in command for d in dangerous):
        return "Error: Dangerous command blocked"
    try:
        r = subprocess.run(command, shell=True, cwd=WORKDIR,
                           capture_output=True, text=True, timeout=120)
        out = (r.stdout + r.stderr).strip()
        return out[:50000] if out else "(no output)"
    except subprocess.TimeoutExpired:
        return "Error: Timeout (120s)"


def run_read(path: str, limit: int = None) -> str:
    """Read file contents, optionally truncating to *limit* lines."""
    try:
        text = safe_path(path).read_text()
        lines = text.splitlines()
        if limit and limit < len(lines):
            lines = lines[:limit] + [f"... ({len(lines) - limit} more lines)"]
        return "\n".join(lines)[:50000]
    except Exception as e:
        return f"Error: {e}"


def run_write(path: str, content: str) -> str:
    """Write *content* to *path*, creating parent directories as needed."""
    try:
        fp = safe_path(path)
        fp.parent.mkdir(parents=True, exist_ok=True)
        fp.write_text(content)
        return f"Wrote {len(content)} bytes to {path}"
    except Exception as e:
        return f"Error: {e}"


def run_edit(path: str, old_text: str, new_text: str) -> str:
    """Replace the first occurrence of *old_text* with *new_text* in *path*."""
    try:
        fp = safe_path(path)
        content = fp.read_text()
        if old_text not in content:
            return f"Error: Text not found in {path}"
        fp.write_text(content.replace(old_text, new_text, 1))
        return f"Edited {path}"
    except Exception as e:
        return f"Error: {e}"


# ęĄęĄ Dispatch map ęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄęĄ
TOOL_HANDLERS = {
    "bash":       lambda **kw: run_bash(kw["command"]),
    "read_file":  lambda **kw: run_read(kw["path"], kw.get("limit")),
    "write_file": lambda **kw: run_write(kw["path"], kw["content"]),
    "edit_file":  lambda **kw: run_edit(kw["path"], kw["old_text"], kw["new_text"]),
}

