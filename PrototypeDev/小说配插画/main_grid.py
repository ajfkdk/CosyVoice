def run_grid_pipeline(novel_text: str):
    os.makedirs(OUTPUT_DIR, exist_ok=True)

    print("=" * 60)
    print("📖 1Prompt1Story - 16宫格故事生成器")
    print("=" * 60)

    # Step 1: 生成连贯叙事prompt
    print("\n[1/3] 宫崎骏分镜师正在提炼16个关键画面...")
    llm = LLMEngine(model=LLM_MODEL, api_key=LLM_API_KEY)

    story_prompt = generate_story_prompt(
        llm, novel_text,
        GLOBAL_SETTINGS["character_card"],
        GLOBAL_SETTINGS["art_style"]
    )

    # 保存完整prompt
    prompt_file = os.path.join(OUTPUT_DIR, "story_prompt.txt")
    with open(prompt_file, "w", encoding="utf-8") as f:
        f.write(story_prompt)
    print(f"✅ 连贯叙事prompt已生成 → {prompt_file}")
    print(f"\n预览：\n{story_prompt[:200]}...\n")

    # Step 2: 收集角色参考图
    ref_images = []
    for name, path in CHARACTER_IMAGES.items():
        if os.path.exists(path):
            ref_images.append(path)
            print(f"✅ 角色参考图：{name}")

    # Step 3: 生成16宫格图
    print(f"\n[2/3] 调用Gemini生成16宫格图...")
    print(f"   参考图数量：{len(ref_images)}")
    print(f"   输出格式：16:9 @ 2K")

    nano = NanobananaEngine()
    grid_output = os.path.join(OUTPUT_DIR, "story_grid_16.png")

    result = nano.generate(
        prompt=story_prompt,
        input_image=ref_images if ref_images else None,
        output_path=grid_output,
        aspect_ratio="16:9",
        image_size="2K"
    )

    if result:
        print(f"\n✅ 16宫格图生成成功 → {result}")
    else:
        print(f"\n❌ 生成失败")

    print("\n" + "=" * 60)
    print("✅ 完成！")
    print("=" * 60)
    return result


